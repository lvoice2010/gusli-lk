const fs = require("fs");
const {
  Document, Packer, Paragraph, TextRun, HeadingLevel, AlignmentType, BorderStyle, ShadingType, PageBreak,
} = require("docx");

const FONT = "Arial";
const out = [];
const H2 = (t) => new Paragraph({ heading: HeadingLevel.HEADING_2, children: [new TextRun(t)] });
function P(runs, opts = {}) {
  const arr = (Array.isArray(runs) ? runs : [runs]).map((r) => (typeof r === "string" ? new TextRun(r) : new TextRun(r)));
  return new Paragraph({ spacing: { after: 100 }, children: arr, ...opts });
}
const b = (text) => ({ text, bold: true });
const t = (text) => ({ text });
// Реплика продавца — цитата-блок с левой границей и подложкой
function say(text) {
  return new Paragraph({
    spacing: { before: 60, after: 120 },
    indent: { left: 240 },
    shading: { fill: "F1FBFC", type: ShadingType.CLEAR, color: "auto" },
    border: { left: { style: BorderStyle.SINGLE, size: 18, color: "1597A8", space: 10 } },
    children: [new TextRun({ text: "💬 " }), new TextRun({ text, italics: false })],
  });
}
// Что показать на экране
function show(text) {
  return new Paragraph({ spacing: { after: 100 }, children: [new TextRun({ text: "🖥 Показать: ", bold: true, color: "6D5CF6" }), new TextRun({ text, italics: true, color: "5B6577" })] });
}
function objection(q) {
  return new Paragraph({ spacing: { before: 100, after: 40 }, children: [new TextRun({ text: "Возражение: ", bold: true, color: "8A3B3B" }), new TextRun({ text: q, bold: true })] });
}
const tip = (text) => new Paragraph({ spacing: { after: 100 }, children: [new TextRun({ text, italics: true, color: "5B6577", size: 19 })] });

// ---- Контент ----
out.push(new Paragraph({ spacing: { after: 40 }, children: [new TextRun({ text: "Скрипт встречи: демонстрация личного кабинета → закрытие сделки", bold: true, size: 32 })] }));
out.push(new Paragraph({ spacing: { after: 30 }, children: [new TextRun({ text: "Голосовой AI-ассистент на входящую линию · для отдела продаж", color: "5B6577", size: 24 })] }));
out.push(P([b("Демо ЛК: "), t("https://gusli-lk.vercel.app   ·   "), b("Тариф: "), t("поддержка 13 800 ₽/мес + 13,5 ₽/мин")]));

out.push(H2("0. Открытие встречи (1–2 мин)"));
out.push(say("[Имя], спасибо, что нашли время. Давайте так: за 15 минут покажу на живом кабинете, как это работает и что вы будете видеть каждый день — на примере реального клиента. По ходу задавайте вопросы, а в конце договоримся о следующем шаге. Хорошо?"));
out.push(tip("Получаем «да» — это рамка встречи и согласие на закрытие в конце."));
out.push(P([b("Закрепить боль (из звонка):")]));
out.push(say("Вы говорили, что [теряются звонки в пик / не успеваете отвечать вечером и в выходные / операторы дорого обходятся / нет понятной картины, о чём звонят]. Сейчас покажу, как ассистент это закрывает — и как вы это контролируете."));

out.push(H2("1. Суть в одном предложении"));
out.push(say("КиберГусли — это голосовой AI-ассистент, который принимает 100% входящих звонков 24/7: отвечает на типовые вопросы, бронирует и записывает, а сложное передаёт оператору. Тариф простой: небольшая фиксированная поддержка плюс оплата минут разговора — без зарплат, графиков и больничных. А этот личный кабинет — ваша полная прозрачность по его работе."));
out.push(show("открыть ЛК → Дашборд"));

out.push(H2("2. Демонстрация кабинета (ядро встречи, 7–10 мин)"));
out.push(tip("Идём сверху вниз, каждую цифру связываем с деньгами или контролем."));
out.push(P([b("Дашборд — сводка:")]));
out.push(say("Вот за месяц: всего обращений, из них 86% ассистент закрыл сам, и только 14% ушло на оператора. Каждое число разворачивается по периодам — день, неделя, месяц, любой диапазон."));
out.push(P([b("ИИ vs оператор + причины:")]));
out.push(say("Видно не просто «передал оператору», а почему: запрос вне сценария, клиент сам попросил человека. Это подсказывает, что дописать в сценарий, чтобы ассистент закрывал ещё больше."));
out.push(P([b("Тематика и конверсия:")]));
out.push(say("О чём звонят — топ-темы. И главное — конверсия в заявки: сколько звонков превратилось в брони/записи. Это прямая связь с вашей выручкой. И эти цифры можно сравнить с показателями ваших операторов — на одних и тех же звонках сразу видно, кто конвертирует лучше."));
out.push(P([b("Почасовая карта нагрузки:")]));
out.push(say("Вот ваши пики. Раньше в эти часы звонки терялись — оператор не успевал. Теперь их принимает ассистент, ни один звонок не пропущен."));
out.push(P([b("ROI / экономия:")]));
out.push(say("А вот в деньгах: сколько времени оператора ассистент сэкономил и сколько это в рублях. Считаем честно — с учётом того, что оператор в разговоре ~70% времени. Вот ваша экономия за период."));
out.push(P([b("Диалоги — прозрачность:")]));
out.push(say("Любой звонок можно открыть: кто звонил, расшифровка диалога, запись, что ассистент сделал в вашей системе. Полный контроль качества — слышно каждое слово."));
out.push(show("открыть карточку вызова → стенограмма + запись"));
out.push(P([b("База знаний:")]));
out.push(say("Ассистент отвечает строго по вашим данным — меню, цены, часы, правила. Вы в любой момент видите, на основе чего он ответил. Нужно изменить — пишете менеджеру, мы обновляем."));
out.push(P([b("Витрина и ИИ-отчёт (задел на будущее):")]));
out.push(say("Здесь — что можно подключить дальше: расширенная аналитика, исходящие кампании, ежеквартальный ИИ-отчёт. Вот пример отчёта — карта проблем и рекомендации с прогнозом роста продаж."));
out.push(show("открыть «Демо» квартального отчёта на 5 секунд"));
out.push(P([b("Документы и менеджер:")]));
out.push(say("Счета и акты — здесь же, ничего не теряется. И ваш персональный менеджер всегда на связи."));

out.push(H2("3. Резюме ценности (перед закрытием)"));
out.push(say("Смотрите, что вы получаете: ассистент на линии 24/7, 100% принятых звонков, экономия на операторах, рост конверсии в заявки и полная прозрачность в этом кабинете. Тариф понятный: фиксированная поддержка + минуты по факту, первый период со скидкой."));

out.push(H2("4. Отработка возражений"));
out.push(objection("«Дорого / надо посчитать»"));
out.push(say("Тариф простой и прозрачный: фиксированная поддержка 13 800 ₽/мес плюс минуты разговора по 13,5 ₽/мин. Смотрите: даже 2000 минут в месяц — а это 30–35 звонков в день — выходит всего ≈ 40 800 ₽/мес. За круглосуточный сервис, 100% принятых звонков и ответ за секунду — это совсем немного, согласитесь? Один оператор обходится дороже и не работает 24/7, без больничных и отпусков."));
out.push(objection("«А вдруг ассистент ошибётся / клиенту не понравится робот»"));
out.push(say("Поэтому и есть кабинет: вы слышите каждый звонок и видите расшифровки. Если что-то не так — правим сценарий за день. А сложное ассистент сразу передаёт человеку."));
out.push(objection("«У нас уже есть операторы»"));
out.push(say("Ассистент их не заменяет, а снимает рутину и пик — операторы занимаются только тем, где реально нужен человек. И вы видите по цифрам, сколько он их разгрузил и кто лучше конвертирует."));
out.push(objection("«Надо подумать / посоветоваться»"));
out.push(say("Конечно. Чтобы думать на фактах, давайте запустим — первый период со скидкой, на ваших реальных звонках. Через пару недель посмотрим этот же кабинет уже с вашими цифрами, и вы решите."));

out.push(H2("5. Закрытие сделки"));
out.push(say("Запуск под ваш бизнес занимает около двух недель — настраиваем сценарии и базу знаний, тестируем. Поэтому чем раньше подпишем договор, тем раньше ассистент выйдет на линию. И вот что важно: если подписываемся на этой неделе — я закрою для вас слот по бесплатному запуску (настройка и подключение за наш счёт). Позже запуск будет стоить 56 300 ₽. Подписываем на этой неделе?"));
out.push(P([b("Если «да»:")]));
out.push(say("Отлично, фиксирую за вами бесплатный запуск! Сейчас соберём: с какого номера принимаем звонки, ваши частые вопросы для базы знаний и реквизиты для договора. Договор и счёт пришлю сегодня — по подписании сразу запускаем настройку и за две недели выходим на линию."));
out.push(P([b("Если колеблется:")]));
out.push(say("Понимаю. Но слот по бесплатному запуску я могу держать только до конца недели — дальше запуск стоит 56 300 ₽. Давайте не терять эти деньги: подпишем договор сейчас, а детали сценария добьём уже в процессе настройки. Ок?"));
out.push(tip("Финальная цель: подписание договора на этой неделе (= бесплатный запуск) + сбор данных для настройки + счёт и договор сегодня."));

const doc = new Document({
  styles: {
    default: { document: { run: { font: FONT, size: 22 } } },
    paragraphStyles: [
      { id: "Heading2", name: "Heading 2", basedOn: "Normal", next: "Normal", quickFormat: true,
        run: { size: 26, bold: true, font: FONT, color: "15306B" },
        paragraph: { spacing: { before: 260, after: 100 }, outlineLevel: 1 } },
    ],
  },
  sections: [{
    properties: { page: { size: { width: 12240, height: 15840 }, margin: { top: 1440, right: 1440, bottom: 1440, left: 1440 } } },
    children: out,
  }],
});

Packer.toBuffer(doc).then((buf) => {
  fs.writeFileSync("Скрипт_встречи_ЛК_КиберГусли.docx", buf);
  console.log("written Скрипт_встречи_ЛК_КиберГусли.docx", buf.length, "bytes");
});
