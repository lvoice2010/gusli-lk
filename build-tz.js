function bullet(runs) {
  const children = (Array.isArray(runs) ? runs : [runs]).map((r) => (typeof r === "string" ? new TextRun(r) : new TextRun(r)));
  return new Paragraph({ numbering: { reference: "b", level: 0 }, spacing: { after: 60 }, children });
}
function num(runs) {
  const children = (Array.isArray(runs) ? runs : [runs]).map((r) => (typeof r === "string" ? new TextRun(r) : new TextRun(r)));
  return new Paragraph({ numbering: { reference: "n", level: 0 }, spacing: { after: 60 }, children });
}
const border = { style: BorderStyle.SINGLE, size: 1, color: "CCCCCC" };
const borders = { top: border, bottom: border, left: border, right: border, insideHorizontal: border, insideVertical: border };
function cell(text, w, { head = false } = {}) {
  return new TableCell({
    width: { size: w, type: WidthType.DXA },
    borders,
    shading: head ? { fill: "E7E9F5", type: ShadingType.CLEAR, color: "auto" } : undefined,
    margins: { top: 60, bottom: 60, left: 110, right: 110 },
    children: [new Paragraph({ children: [new TextRun({ text, bold: head })] })],
  });
}
function table(headRow, rows, widths) {
  const total = widths.reduce((a, c) => a + c, 0);
  return new Table({
    width: { size: total, type: WidthType.DXA },
    columnWidths: widths,
    rows: [
      new TableRow({ tableHeader: true, children: headRow.map((h, i) => cell(h, widths[i], { head: true })) }),
      ...rows.map((r) => new TableRow({ children: r.map((c, i) => cell(c, widths[i])) })),
    ],
  });
}
